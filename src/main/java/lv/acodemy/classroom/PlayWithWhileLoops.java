package lv.acodemy.classroom;

public class PlayWithWhileLoops {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        int j = 2;
        while (j <= 20) {
            System.out.println(j);
            j += 2;

        }
    }
}
